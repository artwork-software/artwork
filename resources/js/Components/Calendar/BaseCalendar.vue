                    @click="cancelMultiEditDuplicateSelection"
                    :disabled="computedCheckedEventsForMultiEditCount === 0"
                    :text="$t('Cancel selection')"/>
            </div>
        </div>
    </div>
    <EventComponent
        v-if="showEventComponent"
        :showHints="usePage().props.show_hints"
        :eventTypes="eventTypes"
